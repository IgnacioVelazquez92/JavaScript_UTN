// Introducción a JavaScript

// JavaScript es un lenguaje de programación que se utiliza principalmente para crear contenido web interactivo.
// Es uno de los lenguajes de programación más populares y es esencial para el desarrollo web moderno.
// JavaScript permite a los desarrolladores crear efectos dinámicos en las páginas web, como animaciones, interacciones con el usuario, y mucho más.

// Características de JavaScript:

// - Lenguaje interpretado: JavaScript es interpretado por el navegador web, lo que significa que el código se ejecuta directamente sin necesidad de compilarse.
// - Basado en prototipos: A diferencia de otros lenguajes orientados a objetos, JavaScript utiliza prototipos en lugar de clases.
// - Dinámico: Las variables en JavaScript son de tipo dinámico, lo que significa que pueden contener diferentes tipos de datos en distintos momentos.
// - Multiplataforma: JavaScript se puede ejecutar en cualquier dispositivo que tenga un navegador web.

// Uso de JavaScript:

// JavaScript se utiliza en una amplia variedad de aplicaciones:
// - Desarrollo web: Interactividad en páginas web, validación de formularios, manipulación del DOM, etc.
// - Aplicaciones móviles: Con frameworks como React Native.
// - Aplicaciones de escritorio: Con herramientas como Electron.
// - Desarrollo de servidores: Utilizando Node.js.

